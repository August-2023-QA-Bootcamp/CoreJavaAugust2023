package lec12_03_java_enumeration;

// Enum names starts with UpperCase 
// They follow Camel case or Snake case pattern.
// CamelCase or Snake_case feature considered from second word, not from first word
// CamelCase example for an Enum --> SizeOfShirts
// Snake_case example for an Enum --> Size_of_shirts

// What is Enum?
// predefined variables represented as bold and italic
// Enum is a constant data type keyword used to declare predefined variables by user.

public enum Size {
	SMALL, 
	MEDIUM, 
	LARGE, 
	XLARGE
}
