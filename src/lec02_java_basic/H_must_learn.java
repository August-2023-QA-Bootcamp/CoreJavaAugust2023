package lec02_java_basic;

public class H_must_learn { // start of the class body
	// This is called class body
	// What a class body contains?
	// First: Class body contain variables
	// Those variables can be either declared (no value assigned) or initialized (value assigned) 
	
	// Line 9-28, is the explanation of line 7
	// here, public is access modifier, String is the type of the variable, myName is the name of the String type variable
	public String myName; // variable is declared, no value assigned
	public String schoolName = "Enthrall IT"; // variable is initialized, a value is assigned
	// here, public is access modifier, String is the type of the variable, schoolName is the name of the variable
	public String family_name;
	// variable starts with lower case and follow camel case and snake case pattern 
	// String type values are always inside double quotation
	
	// int is the type of the variable, myAge is the name of the int type variable
	public int myAge = 100;
	// int type values are with no quotation
	
	// char is the type of the variable, mySex is the name of the char type variable
	public char mySex = 'M';
	// char type values are always inside single quotation
	
	// boolean is the type of the variable, usCitizen is the name of the boolean type variable
	public boolean usCitizen = true;
	// boolean type values are with no quotation. Represented by true or false
	
	// Second: Class body also contains Constructor
	// Those Constructor can be either declared or initialized
	
	// Third: Class body also contains methods
	// Those methods can be either implemented or initialized. (very important info)
	// Inside the class, method can't be declared  (very important info)
	
	
	// After method, main method can come
	public static void main(String[] args) {
		// main method body
	}
	
	
} // End of the class body
